package Encapsulation.example3;

public class Rectangle {
    private int length;
    private int width;

    // Getter for length
    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }
}
